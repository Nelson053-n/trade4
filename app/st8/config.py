"""Конфигурация ST8 — «дивидендный набег» (pre-ex dividend run-up).

Стратегия событийная (дневная гранулярность). Гипотеза (подтверждена бэктестом 08.07.2026,
373 события 2020-2026, комиссия 0.10% RT, 3-way ✅, контроль на случайной дате = ноль):
розница «набегает» на дивидендную акцию ПЕРЕД отсечкой (жадность до выплаты) → цена растёт
на ожидании. Стратегия: КУПИТЬ за entry_days_before торговых дней до ex-даты, ПРОДАТЬ
накануне ex (до дивидендного гэпа). Дивиденд НЕ забираем — держать сквозь гэп нельзя
(разворот в минус, доказано: hold-through = −1.08% t=−3.04).

КАЛИБРОВКА ex-date: реальный гэп на 1 торг.день РАНЬШЕ registryclosedate (T+1 расчёты MOEX).
ex = день гэпа = (первый торг.день >= registryclosedate) − 1. Продаём на close дня перед ex.

ПОРТФЕЛЬ (мой независимый бэктест, N=10, без июля, t>1.5): 16 бумаг ядро, t=8.2, ~23 сд/год,
~78%/год сырых на задействованный капитал. С хеджем IMOEXF и реалистичным проскальзыванием —
оценочно 30-45%/год чистыми. ИЮЛЬ ИСКЛЮЧЁН (основной дивсезон переразогрет: −0.75%).
НЕСЁТ направленную бету 10 дней (2022 −45%) → хедж шортом фьючерса IMOEXF обязателен.
"""
from __future__ import annotations

from pydantic import BaseModel


class St8StrategyConfig(BaseModel):
    """Сигнальная логика ST8 (событийная, дневная)."""
    entry_days_before: int = 10       # купить за N торговых дней до ex-даты (свип: 10 оптимум)
    exit_offset_days: int = 2         # продать за M дней до ex. АНАЛИЗ ВЫХОДА 08.07 (119 событий):
    # ex-2 = +3.62% (win 78%) ОПТИМУМ, ex-1 = +3.30%, ex-3 = +3.32%, ex(гэп) = +1.03%,
    # ex+1(сквозь гэп) = −1.17%. Последний день перед гэпом часть роста уже сдувается →
    # выход за 2 дня строго лучше. НЕ держать до дня отсечки/через гэп.
    skip_july: bool = True            # НЕ торговать отсечки июля (переразогрет, edge отрицателен)
    min_div_yield_pct: float = 1.0    # входить только если дивдоходность к цене >= X% (мелкие мимо)
    # ── ЗАЩИТА ──
    # хедж рыночного риска: одновременно с покупкой акции — ШОРТ фьючерса IMOEXF на
    # эквивалентный нотионал. Убирает бету 10-дневного удержания (2022 без хеджа −45%).
    hedge_imoexf: bool = True
    hedge_ratio: float = 1.0          # доля нотионала позиции, хеджируемая шортом IMOEXF (1.0=полн.)
    # стоп-лосс на сделку: если акция (за вычетом хеджа) упала > stop_loss_pct — выход досрочно
    stop_loss_pct: float = 5.0        # 0 = выключен
    # дневной лимит убытка на движок (₽): накопленный минус за день ниже лимита → HALT входов
    daily_loss_limit_rub: float = 0.0
    fee_rate: float = 0.0005          # комиссия брокера, доля нотионала за операцию (0.05%)
    quantity_lots: int = 1            # базовый размер (лотов акции на событие)
    # фильтр ликвидности исполнения: не входить, если bid/ask спред > max_spread_pct.
    # Замер живых спредов 08.07: медиана ядра 0.043% (крупные <0.02%), НО тонкие
    # MRKC 0.56%/MRKP 0.27%/BELU 0.17% дороги в исполнении → съедают edge. Порог отсекает их.
    max_spread_pct: float = 0.25      # 0 = выключен
    # ── ШОРТ-НОГА «пост-дивидендное сдувание» (найдена 09.07 из гэп-теста) ──
    # Шорт на закрытии ex-дня (ПОСЛЕ гэпа — дивиденд шорт не платит), выкуп через
    # short_hold_days торг.дней. Бэктест 223 события 2020-2025: +2.65%/сд t=6.73 win 69%,
    # ВСЕ 6 лет в плюсе; ИЮЛЬ ЛУЧШИЙ (+4.48% win 81%) — переразогретый дивсезон сдувается.
    # skip_july НЕ применяется к шорту. Декабрь (−0.56%, ралли) и август — фильтр.
    short_enabled: bool = True
    short_hold_days: int = 5          # выкуп через N торговых дней (5 оптимум, 10 хуже)
    short_skip_months: list[int] = [8, 12]   # месяцы ex-даты, когда шорт не входит
    # ── ФЬЮЧЕРСНОЕ ИСПОЛНЕНИЕ (рычаг плана 100%+: ГО 15-20% → плечо ~3× без займа) ──
    # Валидировано 09.07 на 53 событиях: перенос набега акция→фьючерс r=0.894, ratio 0.87,
    # win 77→75. Бумаги с квартальниками FORTS торгуются фьючерсом (резолв ближайшей серии
    # по ASSETCODE, экспирация после окна сделки); без фьючерса — fallback на акцию.
    use_futures: bool = True
    # ── СУММА ВХОДА ПО НОГАМ (сайзинг позиции) ──
    # manual_rub: фикс. нотионал entry_notional_rub на позицию (лоты = сумма/цена/пункт);
    # cash_pct: entry_cash_pct % от СВОБОДНОГО кэша счёта (sandbox: free money API;
    # paper: капитал − занятые нотионалы). 0/сбой → fallback на quantity_lots.
    sizing_mode: str = "manual_rub"   # manual_rub | cash_pct
    entry_notional_rub: float = 100_000.0
    entry_cash_pct: float = 25.0


class St8Config(BaseModel):
    strategy: St8StrategyConfig = St8StrategyConfig()
    mode: str = "paper"               # paper | tbank_sandbox
    account_id: str = ""
    poll_seconds: float = 3600.0      # событийная — раз в час достаточно (проверка календаря)
    trading_enabled: bool = True
