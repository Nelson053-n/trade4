"""Конфигурация ST6 — фандинг-арбитраж вечных фьючерсов MOEX.

Позиция: ШОРТ вечного (получаем фандинг SWAPRATE) + ЛОНГ ближнего квартальника (хедж
рыночного риска; платим базис-decay). Сигнал: edge = аннуализированный трейл-фандинг −
аннуализированный базис квартальника, в процентных пунктах годовых. Ресёрч/бэктест
02.07.2026 (12 мес ISS): IMOEXF ~+38-43% на ГО, edge-фильтр держит вне рынка пары
без альфы (GAZPF) и спящие периоды (SBERF 2026H1).
"""
from __future__ import annotations

from pydantic import BaseModel


class St6StrategyConfig(BaseModel):
    """Сигнальная логика ST6 (дневная гранулярность — решение после вечернего клиринга)."""
    edge_enter_pp: float = 4.0        # вход при edge > 4 пп годовых
    edge_exit_pp: float = 0.0         # выход при edge < 0 (фандинг перестал покрывать базис)
    # ДИВИДЕНДНАЯ ЛОВУШКА: аномальный |базис| (бэквордация из-за дивидендного дисконта
    # квартальника) раздувает edge фиктивным «доходом» — дисконт не конвергирует в прибыль
    # (отсечка вернёт его через ногу перпа). |basis| выше порога → входу нельзя верить.
    basis_sane_pp: float = 25.0
    fund_trail_days: int = 5          # трейл фандинга для сигнала (сглаживание дневного шума)
    roll_days_before: int = 3         # ролл квартальной ноги за N дней до экспирации
    units: int = 1                    # юнитов на пару (юнит = perp_lots+quart_lots из реестра)
    fee_per_lot: float = 2.0          # комиссия ₽/лот (модель; сделки редкие)


class St6Config(BaseModel):
    strategy: St6StrategyConfig = St6StrategyConfig()
    mode: str = "paper"               # paper | tbank_sandbox (боевой st6 — не раньше валидации)
    account_id: str = ""
    poll_seconds: float = 600.0       # дневная стратегия — частый опрос не нужен
    trading_enabled: bool = True
