"""Конфигурация ST7 — «фандинг-давление»: полухеджированный шорт вечного фьюча.

⚠️ ЧЕСТНАЯ ПЕРЕОЦЕНКА 07.07.2026 (агент-аудит на 365д с раздельным учётом фандинг/цена):
ПРЕЖНИЕ claim'ы (IMOEXF +37.7%, GAZPF +41.6%, «USDRUBF 114% на ГО») НЕ ПОДТВЕРДИЛИСЬ —
завышены в 3-8×. Это НЕ рыночно-нейтральный carry, а ЗАМАСКИРОВАННЫЙ НАПРАВЛЕННЫЙ ШОРТ:
IMOEXF/GAZPF плюсовали только потому, что рынок ПАДАЛ (IMOEXF −25%, GAZPF −35% за период);
на развороте вверх ценовой компонент станет минусом. Реальный структурный edge = только
~9-12%/год фандинга, он НЕ перекрывает carry+комиссии+хвостовой риск. USDRUBF/EURRUBF
УБЫТОЧНЫ даже в удачный год. GAZPF «128%» = 3 сделки (нет статистики). st7 ОСТАНОВЛЕНА
07.07 — на бой по этим числам не выводить. Плюс: sandbox НЕ начисляет SWAPRATE
(доказано на st6), поэтому фандинг-edge в песочнице непроверяем в принципе.
Гипотеза (исходная): аномально высокий фандинг = толпа в лонгах = перегрев → слабость.
Позиция: ШОРТ вечного (2×паритет) + ЛОНГ квартальника (1×) — полухедж.
"""
from __future__ import annotations

from pydantic import BaseModel


class St7StrategyConfig(BaseModel):
    """Сигнальная логика ST7 (дневная гранулярность, решение после вечернего клиринга)."""
    fund_enter_pp: float = 35.0       # вход: 3д-средний аннуализ. фандинг > 35 пп годовых
    fund_exit_pp: float = 25.0        # выход: < 25 пп (давление спало)
    fund_trail_days: int = 3          # трейл фандинга
    roll_days_before: int = 3         # ролл квартальной ноги за N дней до экспирации
    units: int = 1                    # юнитов на пару (юнит = perp_lots+quart_lots из реестра)
    fee_per_lot: float = 2.0
    # дивидендный фильтр (как st6): аномальный |базис| квартальника → входу нельзя верить
    basis_sane_pp: float = 25.0
    # ── ЗАЩИТА ОТ ДЕВАЛЬВАЦИОННОГО ГЭПА (полухедж снимает половину, но не стоп) ──
    # 1) стоп-лосс: unrealized позиции (P&L ног + фандинг − комиссии) < −stop_loss_pct
    #    от entry-нотионала перп-ноги → аварийный выход НЕЗАВИСИМО от фандинга.
    #    Полухедж уже режет движение вдвое, так что 10% убытка ≈ ~20% хода цены.
    stop_loss_pct: float = 10.0       # 0 = выключен
    # 2) гейт широкого гэпа: если перп двинулся > gap_block_pct за день ПРОТИВ шорта
    #    (перп вырос) — не входить в новую позицию (толпа могла развернуться на панике).
    gap_block_pct: float = 7.0        # 0 = выключен
    # 3) дневной лимит убытка на движок (₽): суммарный минус закрытых сделок за день
    #    ниже −daily_loss_limit_rub → HALT новых входов до след. дня. 0 = выключен.
    daily_loss_limit_rub: float = 0.0


class St7Config(BaseModel):
    strategy: St7StrategyConfig = St7StrategyConfig()
    mode: str = "paper"               # paper | tbank_sandbox
    account_id: str = ""
    poll_seconds: float = 600.0
    trading_enabled: bool = True
